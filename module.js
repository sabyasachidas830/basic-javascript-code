function math(a,b, operator){
    if(operator === "+"){
        console.log(a+b)
    }
    else if(operator === "-"){
        console.log(a-b)
    }
    else if(operator ==="*"){
        console.log(a*b)
    }
    else if(operator ==="/"){
        console.log(a/b)
    }

}
console.log(math(23,23,"*"))